package lab5_bai3;

public class Product {
		String ten;
	    double gia;
	    public Product(){}

	    public Product(String ten, double gia) {
	        this.ten = ten;
	        this.gia = gia;
	    }

	    public String getTen() {
	        return ten;
	    }

	    public double getGia() {
	        return gia;
	}
}
